package com.example.timetable;

public class Teacher {
    //Fields
    private String lessonNumber;
    private String nameOfClass;
    private String timeLesson;
    private String serNameTeacher;
    private String cabinetNumber;
    private String lesson;

    //Functions
    public String getLessonNumber() {
        return lessonNumber;
    }

    public void setLessonNumber(String lessonNumber) {
        this.lessonNumber = lessonNumber;
    }

    public String getNameOfClass() {
        return nameOfClass;
    }

    public void setNameOfClass(String nameOfClass) {
        this.nameOfClass = nameOfClass;
    }

    public String getTimeLesson() {
        return timeLesson;
    }

    public void setTimeLesson(String timeLesson) {
        this.timeLesson = timeLesson;
    }

    public String getSerNameTeacher() {
        return serNameTeacher;
    }

    public void setSerNameTeacher(String serNameTeacher) {
        this.serNameTeacher = serNameTeacher;
    }

    public String getCabinetNumber() {
        return cabinetNumber;
    }

    public void setCabinetNumber(String cabinetNumber) {
        this.cabinetNumber = cabinetNumber;
    }

    public String getLesson() {
        return lesson;
    }

    public void setLesson(String lesson) {
        this.lesson = lesson;
    }
}
